# -*- coding: utf-8 -*-
"""Детерминированное удаление слов-паразитов и звуков-заминок — БЕЗ ИИ.

Работает независимо от gemma (флаг output.remove_fillers). Когда ИИ-очистка
выключена, GigaAM v3 уже расставил пунктуацию и заглавные — остаётся только
вычистить «ну / короче / типа» и «э-э / эм / мм». Скрипт ТОЛЬКО удаляет токены
из фиксированного списка: ничего не выдумывает, не меняет форму и не переставляет
слова (в духе «лучше плохая пунктуация, чем ИИ придумает»). Пунктуацию движка
сохраняет — чистит лишь пробелы/запятые вокруг вырезанного и заглавную в начале.

КОНСЕРВАТИВНО по составу: берём только высоконадёжные паразиты. Спорные «вот»,
«значит», «то есть», «слушай», «понимаешь» НЕ трогаем — без контекста их легко
удалить ошибочно («вот этот дом», «то есть результат»). Звуки-заминки безопасны
всегда.
"""
from __future__ import annotations

import re

# ── 1. Звуки-заминки (всегда безопасно удалять) ──────────────────────────────
# Тянущиеся и дефисные: ээ, э-э, э-э-э, мм, м-м, аа, а-а, эм, эмм, мда.
_HESITATION = re.compile(
    r"(?<![а-яёa-z])"
    r"(?:э(?:[\s-]*э)+|м(?:[\s-]*м)+|а(?:[\s-]*а)+|э+м+|мда|э)"
    r"(?![а-яёa-z])",
    re.IGNORECASE)

# ── 2. Слова-паразиты (высоконадёжные) ───────────────────────────────────────
_FILLER_WORDS = ["короче", "типа", "походу", "блин", "мол", "ну"]
_FILLER_PHRASES = ["как бы", "это самое"]   # многословные идут как \s+-последовательности

_ALT = "|".join([re.escape(w) for w in _FILLER_WORDS]
                + [r"\s+".join(re.escape(p) for p in ph.split())
                   for ph in _FILLER_PHRASES])
_FILLER_TOKEN = r"(?<![а-яёa-z])(?:" + _ALT + r")(?![а-яёa-z])"

_FILLER_RE = re.compile(_FILLER_TOKEN, re.IGNORECASE)
# Парентетический паразит «, X,» — вырезаем вместе с ПАРОЙ запятых («Я, походу,
# забыл» → «Я забыл», а не «Я, забыл»).
_PAREN_RE = re.compile(r",\s*" + _FILLER_TOKEN + r"\s*,", re.IGNORECASE)
# Остальные — вместе с одной хвостовой запятой («Ну, я» → «я»).
_TRAIL_RE = re.compile(_FILLER_TOKEN + r"\s*,?", re.IGNORECASE)


def has_fillers(text: str) -> bool:
    """Быстрая проверка — есть ли что вырезать (чтобы не трогать чистый текст)."""
    return bool(text) and (bool(_HESITATION.search(text)) or bool(_FILLER_RE.search(text)))


def _recap(t: str) -> str:
    """Вернуть заглавную в начало строки и после . ! ? … — мы могли срезать
    первое (с большой буквы) слово предложения («Ну, я…» → «я…» → «Я…»)."""
    t = re.sub(r"^(\W*)([а-яёa-z])",
               lambda m: m.group(1) + m.group(2).upper(), t)
    t = re.sub(r"([.!?…]\s+)([а-яёa-z])",
               lambda m: m.group(1) + m.group(2).upper(), t)
    return t


def strip_fillers(text: str) -> str:
    """Убрать заминки и паразиты, сохранив пунктуацию движка. Если убирать нечего
    — вернуть текст как есть (быстрый путь)."""
    if not has_fillers(text):
        return text

    t = _HESITATION.sub("", text)        # 1) звуки-заминки
    t = _PAREN_RE.sub(" ", t)            # 2) парентетические «, X,» → пробел (обе запятые)
    t = _TRAIL_RE.sub("", t)             # 3) остальные + хвостовая запятая

    # 4) Чистка следов.
    t = re.sub(r"[ \t]+", " ", t)                       # одиночные пробелы
    t = re.sub(r"\s*,(?:\s*,)+", ", ", t)               # серии запятых → одна
    t = re.sub(r"\s+([,.!?…:;])", r"\1", t)             # пробел перед знаком
    t = re.sub(r"[,;:]\s*([.!?…])", r"\1", t)           # запятая перед концом предложения
    t = re.sub(r"(^|[.!?…]\s*)[,;:]\s*", r"\1", t)      # запятая в начале предложения
    t = re.sub(r"^[\s.,;:…!?\-–—]+", "", t)             # ведущая «осиротевшая» пунктуация
    t = re.sub(r"\(\s+", "(", t)
    t = re.sub(r"[ \t]{2,}", " ", t).strip()

    t = _recap(t)
    # Если вдруг вычистили всё (текст был сплошь паразиты) — вернуть оригинал.
    return t if t.strip() else text
