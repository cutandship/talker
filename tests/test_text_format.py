# -*- coding: utf-8 -*-
"""Голосовое форматирование (text_format.py): команды «пункт один», «тире»,
«новый абзац» → списки/абзацы; обычная речь с теми же словами — НЕ ломается.
Кейсы перенесены из _test_textformat.py (бывший ручной скрипт)."""
import pytest

from text_format import apply_formatting as F, has_format_commands as H

GOOD = [
    ("Пункт один, купить молоко. Пункт два, позвонить маме.",
     "1. Купить молоко.\n2. Позвонить маме."),
    ("Задачи. Тире протестировать. Тире задеплоить.",
     "Задачи.\n— Протестировать.\n— Задеплоить."),
    ("Первая мысль. Новый абзац. Вторая мысль.",
     "Первая мысль.\n\nВторая мысль."),
    ("Адрес. Новая строка. Улица Ленина.",
     "Адрес.\nУлица Ленина."),
    ("Первое купить молоко, второе сходить в магазин, третье купить капусту.",
     "1. Купить молоко.\n2. Сходить в магазин.\n3. Купить капусту."),
    ("Первое сделать дизайн второе написать код",
     "1. Сделать дизайн.\n2. Написать код."),
    # регрессия: подряд идущие ординалы без текста между
    ("первое, второе, третье", "1.\n2.\n3."),
]

# Обычная речь со словами «пункт»/«тире»/«первое» — НЕ должна разрываться.
TRAPS = [
    "Мы обсудили этот пункт договора подробно.",
    "Я живу в пункте два, это район такой.",
    "Поставь здесь тире между словами.",
    "Это компания тире лидер рынка.",
    "Первое впечатление самое важное.",
    "Первое сентября начало учебного года.",
    "Второе блюдо было вкуснее первого.",
]


@pytest.mark.parametrize("inp,expected", GOOD)
def test_formats_commands(inp, expected):
    assert F(inp).strip() == expected.strip()


@pytest.mark.parametrize("inp", TRAPS)
def test_plain_speech_untouched(inp):
    got = F(inp)
    assert "\n" not in got
    assert not got.lstrip().startswith(("1.", "—"))


def test_detector():
    assert not H("обычный текст без команд")
    assert H("Пункт три, сделать что-то.")
    assert H("Новый абзац здесь.")
