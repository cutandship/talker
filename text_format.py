# -*- coding: utf-8 -*-
"""Голосовое форматирование структуры текста (списки, абзацы) по командам.

Детерминированный парсер: НЕ ИИ. Ловит проговорённые маркеры структуры в уже
распознанном (и пунктуированном движком) тексте и превращает их в реальное
форматирование. Никогда не выдумывает и не меняет слова — только режет на
строки/пункты по командам.

Команды (произносятся вслух во время диктовки):
    «пункт один … пункт два …»      → нумерованный список  1. … 2. …
    «тире …» / «дефис …»             → маркированный список  — …
    «новый абзац»                    → пустая строка между блоками
    «новая строка»                   → перенос строки

ВАЖНО про ложные срабатывания: эти маркеры — обычные русские слова, поэтому
включается форматирование флагом (output.voice_formatting) и его можно
переключить для каждой записи в истории. Сам парсер старается не срабатывать
там, где слово стоит в падеже («в пункте два», «этот пункт договора»): команда
ловится только в форме «пункт <число/порядковое>» в именительном.
"""
from __future__ import annotations
import re

# Числительные/порядковые → цифра (для «пункт три» → «3.»).
_NUM_WORDS = {
    "один": 1, "одна": 1, "два": 2, "две": 2, "три": 3, "четыре": 4,
    "пять": 5, "шесть": 6, "семь": 7, "восемь": 8, "девять": 9, "десять": 10,
    "первый": 1, "второй": 2, "третий": 3, "четвёртый": 4, "четвертый": 4,
    "пятый": 5, "шестой": 6, "седьмой": 7, "восьмой": 8, "девятый": 9,
    "десятый": 10,
}
_NUM_ALT = "|".join(sorted(_NUM_WORDS, key=len, reverse=True))

# «пункт <число-словом или цифрой>» — ТОЛЬКО именительный «пункт» (не «пункте»,
# «пункта», «пунктом»), чтобы «в пункте два» не сработало. Требуем число сразу
# после — иначе это просто существительное («обсудили этот пункт договора»).
_RE_NUM_ITEM = re.compile(
    r"[.,;:]?\s*\bпункт\s+(" + _NUM_ALT + r"|\d{1,2})\b[.,]?\s*",
    re.IGNORECASE)

# «новый абзац» / «новая строка» — устойчивые двусловные команды.
_RE_PARA = re.compile(r"[.,;:]?\s*\bновый\s+абзац\b[.,]?\s*", re.IGNORECASE)
_RE_NEWLINE = re.compile(r"[.,;:]?\s*\bновая\s+строка\b[.,]?\s*", re.IGNORECASE)

# Порядковые-средний-род как маркеры списка БЕЗ слова «пункт» («первое… второе…»).
# Опасны (ломают «первое впечатление»), поэтому ловим ТОЛЬКО когда их 2+ в тексте
# (настоящий список почти всегда содержит первое+второе). Считаем по среднему
# роду «-ое/-ье» в начале фрагмента (после точки/запятой/;/тире/начала).
_ORD_N = {"первое":1,"второе":2,"третье":3,"четвёртое":4,"четвертое":4,"пятое":5,
          "шестое":6,"седьмое":7,"восьмое":8,"девятое":9,"десятое":10}
_ORD_ALT = "|".join(sorted(_ORD_N, key=len, reverse=True))
# Маркер пункта. Два варианта позиции:
#  - после разделителя (. , ; : — \n или начало) — обычный случай;
#  - просто как отдельное слово в потоке (STT часто без запятых: «дизайн второе
#    написать») — но это терпимо ТОЛЬКО потому, что ординалы применяются лишь
#    при серии 2+, что само по себе отсекает одиночные «первое впечатление».
_RE_ORD_ITEM = re.compile(
    r"(^|[\n.,;:—-]\s*|\s)\b(" + _ORD_ALT + r")\b",
    re.IGNORECASE)

# «тире»/«дефис» как маркер списка — ловим осторожно: только когда стоит
# В НАЧАЛЕ фрагмента (после точки/переноса/в начале строки), а не в середине
# фразы («компания тире лидер» НЕ должно ломаться). Реализуем отдельно ниже.
_FORMAT_VOCAB = ("пункт", "новый абзац", "новая строка", "тире", "дефис")


def has_format_commands(text: str) -> bool:
    """Быстрая проверка — есть ли в тексте хоть один маркер форматирования.
    Помогает не трогать обычный текст вовсе."""
    low = (text or "").lower()
    if _RE_NUM_ITEM.search(text) or _RE_PARA.search(text) or _RE_NEWLINE.search(text):
        return True
    # тире/дефис в начале предложения
    if re.search(r"(?:^|[.!?]\s+)(?:тире|дефис)\s+", low):
        return True
    # «первое… второе…» — только если их 2+ (серия = настоящий список)
    if len(_RE_ORD_ITEM.findall(text)) >= 2:
        return True
    return False


def _cap_first(s: str) -> str:
    s = s.strip()
    if len(s) >= 1 and s[0].islower():
        return s[0].upper() + s[1:]
    return s


def apply_formatting(text: str) -> str:
    """Применить голосовые команды форматирования. Если команд нет — вернуть
    текст без изменений (быстрый путь)."""
    if not text or not has_format_commands(text):
        return text

    t = text

    # 1) Нумерованные пункты: «пункт N» → перенос + «N. »
    def _num_repl(m: re.Match) -> str:
        w = m.group(1).lower()
        n = _NUM_WORDS.get(w) or (int(w) if w.isdigit() else None)
        return f"\n{n}. " if n is not None else m.group(0)
    t = _RE_NUM_ITEM.sub(_num_repl, t)

    # 1b) «первое… второе…» БЕЗ слова «пункт» — только если серия 2+.
    if len(_RE_ORD_ITEM.findall(t)) >= 2:
        def _ord_repl(m: re.Match) -> str:
            n = _ORD_N.get(m.group(2).lower())
            return f"\n{n}. " if n else m.group(0)
        t = _RE_ORD_ITEM.sub(_ord_repl, t)

    # 2) Абзацы и переносы.
    t = _RE_PARA.sub("\n\n", t)
    t = _RE_NEWLINE.sub("\n", t)

    # 3) «тире/дефис» как маркер списка — ТОЛЬКО в начале предложения/строки
    #    (после точки/!/?/переноса или в самом начале). «Компания тире лидер»
    #    в середине фразы НЕ трогаем.
    t = re.sub(r"(^|\n|[.!?]\s+)\s*(?:тире|дефис)\s+", r"\n— ",
               t, flags=re.IGNORECASE)

    # 4) Уборка: схлопнуть пробелы и тройные переносы, обрезать пробелы у строк.
    t = re.sub(r"[ \t]+", " ", t)
    t = re.sub(r"\n{3,}", "\n\n", t)
    lines = [ln.strip() for ln in t.split("\n")]

    # 5) Капитализация начала каждой непустой строки; убрать висящую точку у
    #    последнего пункта списка (если строка — элемент списка «N.» или «—»).
    def _ensure_dot(s: str) -> str:
        # вернуть концевую точку, которую съел маркер-разделитель (но не дублировать
        # ! ? … : и не ставить точку после открытого предложения с запятой)
        s = s.rstrip()
        if s and s[-1] not in ".!?…:,":
            return s + "."
        return s

    out: list[str] = []
    for ln in lines:
        if not ln:
            out.append("")
            continue
        # элемент нумерованного списка: «3. текст» — капитализируем + точка
        m = re.match(r"^(\d{1,2}\.\s+)(.*)$", ln)
        if m:
            out.append(m.group(1) + _ensure_dot(_cap_first(m.group(2))))
            continue
        # маркер списка «— текст»
        m = re.match(r"^(—\s+)(.*)$", ln)
        if m:
            out.append(m.group(1) + _ensure_dot(_cap_first(m.group(2))))
            continue
        out.append(_ensure_dot(_cap_first(ln)))

    result = "\n".join(out).strip()
    return result
